//Write a Java program that demonstrates the use of the post-increment and pre-increment operators.


public  class  View
{
	public  static  void  main(String[] args) 
	{       
        		int  x  =  10;       
       	                  int  y  =  ++x;       
      		System.out.println("Y  value  is =  " + y);  
		
		int  a  =  20;       
       	                  int  b  =  a++;       
      		System.out.println("B  value  is =  " + b);  
	}

}